const moongose = require('mongoose');

const SubjectSchema = new moongose.Schema({
    name: {
        type: String,
        required: true
    }, 
    dt_creation: {
        type: Date,
        default: Date.now,
        required: true
    }, 
    active: {
        type: Boolean,
        default: true,
        required: true
    }   
});

module.exports = moongose.model('Subject', SubjectSchema);